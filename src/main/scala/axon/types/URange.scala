package axon.types

import chisel3._

/**
 * Represents an unsigned integer range.
 *
 * @param w The width of the start/end range value.
 */
class URange(w: Int) extends Bundle {
  /** Start value */
  val from = UInt(w.W)
  /** End value */
  val to = UInt(w.W)

  override def cloneType: this.type = new URange(w).asInstanceOf[this.type]
}

object URange {
  def apply(r: Range) = {
    val from = r.start.U
    val to = r.last.U
    val w = Math.max(from.getWidth, to.getWidth)
    val wire = Wire(new URange(w))
    wire.from := from
    wire.to := to
    wire
  }

  implicit def range2URange(r: Range): URange = URange(r)
}

